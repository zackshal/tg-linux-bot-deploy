"""Регулярные выражения для поиска email-адресов и телефонных номеров."""

import re

# email: стандартная проверка
EMAIL_REGEX = re.compile(r"\b[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Z|a-z]{2,}\b")
# номер телефона: +7/8 с различными разделителями
PHONE_REGEX = re.compile(
    r"(?:\+7|8)\s*"  # код страны
    r"(?:\(?\d{3}\)?)\s*"  # код оператора
    r"(?:\d{3}[\s-]?\d{2}[\s-]?\d{2}"  # XXX-XX-XX
    r"|\d{7}"  # или 7 цифр подряд
    r")"
)
